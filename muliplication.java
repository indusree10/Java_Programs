package Simple;
import java.util.*;

public class muliplication {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter a number");
		int i = sc.nextInt();
		
		for(int j=1;j<=10;j++) {
			int result = i * j;
			System.out.println(result);
			
		}

	}

}
